# Автоматизація опрацювання елементів словників за допомогою циклу
''' Задача:
Вивести всі пари “ключ-значення” із словника
Розв'язок задачі:
В циклі для змінної key, що набуває значення
кожного ключа словника, по черзі:
виводимо отриманий ключ та його значення
'''

user = {
    "name": "Sergii",
    "age": 100500,
    "profession": "Golf Player",
    "country": "Ukraine"
}

for key in user:
    print(key, user.get(key))