'''
Реалізувати програму, яка виводить на екран квадрат парних чисел з діапазону від 1 до введеного користувачем значення включно.

Додаткові умови:

Результат роботи програми - послідовність чисел;
Використати цикл for
Введене користувачем значення зберігається в змінній number
'''

number = int(input("Введіть число"))

for i in range(1, number + 1):
    if i % 2 == 0:
        print(i ** 2)